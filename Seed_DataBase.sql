DROP TABLE IF EXISTS
    classroomstudents,
    ownedshopitems,
    avatarconfigs,
    transactions,
    attendancerecords,
    weeklyassignmenttemplates,
    schedule,
    shopitems,
    manualcoinadjustments,
    avatarbaseimages,
    users,
    classrooms;

CREATE TABLE users (
    userid text PRIMARY KEY,
    name text NOT NULL,
    role text NOT NULL,
    email text NOT NULL,
    passwordhash text NOT NULL,
    classroomid text
);

CREATE TABLE classrooms (
    id text PRIMARY KEY,
    name text NOT NULL,
    teacherid text
);

CREATE TABLE classroomstudents (
    classroomid text NOT NULL,
    studentid text NOT NULL,
    PRIMARY KEY (classroomid, studentid)
);

CREATE TABLE shopitems (
    id text PRIMARY KEY,
    name text NOT NULL,
    price integer NOT NULL,
    description text NOT NULL
);

CREATE TABLE ownedshopitems (
    userid text NOT NULL,
    shopitemid text NOT NULL,
    PRIMARY KEY (userid, shopitemid)
);

CREATE TABLE avatarconfigs (
    userid text PRIMARY KEY,
    base text,
    hairstyle text,
    clothing text,
    accessory text
);

CREATE TABLE transactions (
    transactionid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    userid text NOT NULL,
    amount integer NOT NULL,
    timestamp timestamp with time zone NOT NULL,
    description text NOT NULL
);

CREATE TABLE attendancerecords (
    userid text NOT NULL,
    classroomid text NOT NULL,
    presentdates text,
    absentdates text,
    PRIMARY KEY (userid, classroomid)
);

CREATE TABLE schedule (
    scheduleid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    classroomid text NOT NULL,
    dayofweek text NOT NULL,
    starttime text NOT NULL,
    endtime text NOT NULL,
    doubleday boolean NOT NULL
);

CREATE TABLE weeklyassignmenttemplates (
    weeklyassignmenttemplateid integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    classroomid text NOT NULL,
    dueweekday smallint NOT NULL CHECK (dueweekday BETWEEN 0 AND 6),
    subject text NOT NULL,
    title text NOT NULL,
    duetime time(0) without time zone NOT NULL,
    displayorder integer NOT NULL DEFAULT 0,
    UNIQUE (classroomid, dueweekday, subject, title)
);

